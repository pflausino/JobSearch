    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (formValidate()) {
            

            curriculum.setFullName(txtName.getText().trim());
            curriculum.setEmail(txtEmail.getText());
            curriculum.setCelular(txtCelular.getText());
            curriculum.setNacionalidade(cbNascionalidade.getSelectedItem().toString());
            curriculum.setIdade((Integer) jSpinner1.getValue());
            curriculum.setObjetivo(txtGoal.getText());
                    
            curriculum.setPresentation(txtApresentation.getText());
            
            curriculum.setQualifications(new ArrayList<Qualification>());
            
            
            for (int i = 0; i < qList.getModel().getSize(); i++) {
                Qualification q = (Qualification) qList.getModel().getElementAt(i);
                curriculum.setQualifications(q);
            }
            
            if(curriculum.getId() == 0){
                try{
                    curriculumDao.insert(curriculum);
                    JOptionPane.showMessageDialog(this, "Sua Curriculo Foi Cadastrado"
                    , "Concluido", JOptionPane.INFORMATION_MESSAGE);
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(this, "Erro ao inserir Cadastro."
                    + "\n" +ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                try{
                    curriculumDao.update(curriculum);
                    JOptionPane.showMessageDialog(this, "Seu Curriculo Foi Cadastrado"
                    , "Concluido", JOptionPane.INFORMATION_MESSAGE);
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(this, "Erro ao alterar Cadastro."
                    + "\n" +ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
        loadTable(curriculum.getEmail());
        enableForm(false);
    }//GEN-LAST:event_btSalvarActionPerformed

