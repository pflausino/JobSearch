     private void loadComboQualification(){
        
        QualificationDao qualificaitonDao = new QualificationDao();
        ArrayList<Qualification> qualificationList = new ArrayList<Qualification>();

        try{
            qualificationList = qualificaitonDao.listAll();
            
            for (Qualification q : qualificationList) {
                cb_Qualification.addItem(q);
            };
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao carregar Qualificações."
                    + "\n" +ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
     private boolean formValidate(){
        if(txtName.getText().trim().length() < 2){
            JOptionPane.showMessageDialog(this, "Nome inválido", "Alerta",
            JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if(txtEmail.getText().trim().length() < 2){
            JOptionPane.showMessageDialog(this, "Email inválido", "Alerta",
            JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if(txtApresentation.getText().trim().length() < 2){
            JOptionPane.showMessageDialog(this, "Apresentação Invalida", "Alerta",
            JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }
     
     private void loadTable(String email){
        CurriculumTableModel tm = (CurriculumTableModel) jTable1.getModel();
        
        try{
            tm.setDados(curriculumDao.GetByEmail(email));
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Erro ao carregar a grade."
                    + "\n" +ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
     

